public class TipCalculatorCPU {
    private float mBill;
    private float mTipPercentage;
    private float mTipAmount;
    private float mTotalBill;

    public float getmBill() {
        return mBill;
    }

    public void setmBill(float mBill) {
        this.mBill = mBill;
    }

    public float getmTipPercentage() {
        return mTipPercentage;
    }

    public void setmTipPercentage(float mTipPercentage) {
        this.mTipPercentage = mTipPercentage;
    }

    public float getmTipAmount() {
        return mTipAmount;
    }

    public void setmTipAmount(float mTipAmount) {
        this.mTipAmount = mTipAmount;
    }

    public float getmTotalBill() {
        return mTotalBill;
    }

    public void setmTotalBill(float mTotalBill) {
        this.mTotalBill = mTotalBill;
    }

}
